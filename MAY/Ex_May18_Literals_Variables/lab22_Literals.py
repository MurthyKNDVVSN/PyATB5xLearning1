#  WAP to take 2 user input and then sum, MUL, DIV of these two numbers and print them
num1 = int(input("Enter the value for num1 ; "))
num2 = int(input(" enter the value for num2 :"))

print(type(num1))
print(type(num2))

SUM = num1 + num2
SUB = num1 - num2
MUL = num1 * num2
DIV = num1 / num2

print("The SUM of num1 & num2 is :", SUM)
print("The SUB of num1 & num2 is :", SUB)
print("The MUL of num1 & num2 is :", MUL)
print("The DIV of num1 & num2 is :", DIV)



